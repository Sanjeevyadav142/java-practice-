package Question3;
// Question 4 -- Write a program to calculate the area of a rectangle using length and breadth variables.
// Answer 
public class areaofrectangle {
    public static void main(String[] args) {
        int length = 30;
        int breadth = 40;
        System.out.println("Lenth of the rectangle = " + length);
        System.out.println("Breadth of the rectangle = " + breadth);
        System.out.println("Area of the rectangle = "+(length*breadth));
    }
}
